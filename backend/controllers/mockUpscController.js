const EXAM_DURATION = 7200; // 120 minutes in seconds per paper

const SECTIONS = [
  { name: "GS Paper I", totalQuestions: 100, correctMarks: 2.0, wrongMarks: -0.66 },
  { name: "CSAT Paper II", totalQuestions: 80, correctMarks: 2.5, wrongMarks: -0.66 }
];

export const getUPSCQuestions = (req, res) => {
  try {
    const sections = SECTIONS.map((sec) => {
      const qs = UPSC_QUESTIONS
        .filter((q) => q.section === sec.name)
        .map(({ correctOption, explanation, ...rest }) => ({
          ...rest,
        }));
      return { name: sec.name, questions: qs, duration: EXAM_DURATION };
    });

    res.json({ sections, duration: EXAM_DURATION });
  } catch (err) {
    console.error("getUPSCQuestions error:", err);
    res.status(500).json({ message: "Failed to load UPSC questions from server." });
  }
};

export const submitExam = (req, res) => {
  try {
    const { answers = {} } = req.body;

    let totalScoreGS = 0;
    let totalScoreCSAT = 0;
    
    let totalCorrect = 0, totalIncorrect = 0, totalSkipped = 0;
    const sectionBreakdown = [];
    const results = [];

    SECTIONS.forEach((sec) => {
      const qs = UPSC_QUESTIONS.filter((q) => q.section === sec.name);

      let sCorrect = 0, sIncorrect = 0, sSkipped = 0, sScore = 0;

      qs.forEach((q) => {
        const key = q.id;
        const userAnswer = answers[key] || null;
        let status, marks;

        if (!userAnswer) {
          status = "skipped";   marks = 0;          sSkipped++;
        } else if (userAnswer === q.correctOption) {
          status = "correct";   marks = sec.correctMarks; sCorrect++;
        } else {
          status = "incorrect"; marks = sec.wrongMarks;   sIncorrect++;
        }

        sScore += marks;
        results.push({
          id:            key,
          section:       sec.name,
          title:         q.title,
          description:   q.description,
          correctOption: q.correctOption,
          explanation:   q.explanation,
          userAnswer,
          status,
          marks,
        });
      });

      totalCorrect   += sCorrect;
      totalIncorrect += sIncorrect;
      totalSkipped   += sSkipped;
      
      if (sec.name === "GS Paper I") totalScoreGS = sScore;
      if (sec.name === "CSAT Paper II") totalScoreCSAT = sScore;

      sectionBreakdown.push({
        name:      sec.name,
        score:     parseFloat(sScore.toFixed(2)),
        correct:   sCorrect,
        incorrect: sIncorrect,
        skipped:   sSkipped,
        total:     qs.length,
        maxScore:  qs.length * sec.correctMarks,
      });
    });

    const isCSATPassed = totalScoreCSAT >= 66;

    res.json({
      scoreGS:          parseFloat(totalScoreGS.toFixed(2)),
      scoreCSAT:        parseFloat(totalScoreCSAT.toFixed(2)),
      maxScoreGS:       200,
      maxScoreCSAT:     200,
      isCSATPassed,
      correctCount:   totalCorrect,
      incorrectCount: totalIncorrect,
      skippedCount:   totalSkipped,
      sectionBreakdown,
      results,
    });
  } catch (err) {
    console.error("submitExam error:", err);
    res.status(500).json({ message: "Failed to submit exam." });
  }
};

export const startExam = getUPSCQuestions;
